    public void submit() throws SQLException {
        Connection con = ConnectionProvider.getconnection();
        Statement statement = con.createStatement();
        try {
            Cipher cipher = new Cipher();
            String query = "Select student_password FROM student WHERE student_id = " + idfield.getText() + "";
            ResultSet result = statement.executeQuery(query);
            if (result.next()) {
                int dialogResult = JOptionPane.showConfirmDialog(this, "Membership Fee : $10\nConfirm Payment?", "Fee Confirmation", JOptionPane.YES_NO_OPTION);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    Vip vip = new Vip(idfield.getText());
                    try {
                        vip.addstudent();
                        StudentDashboard db = new StudentDashboard();
                        db.show();
                        dispose();
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(this, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Membership Rejected.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Wrong Password !!!");
            }
        } catch (Exception e) {
        } finally {
            con.close();
            statement.close();
        }
    }

