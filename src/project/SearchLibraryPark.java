    public void printtable() throws SQLException {
        Cipher c1 = new Cipher();
        Connection con = ConnectionProvider.getconnection();
        Statement statement = con.createStatement();
        try {
            ResultSet result2 = statement.executeQuery(query1);
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);

            while (result2.next()) {
                model.addRow(new String[]{c1.decode(Integer.toString(result2.getInt(1))), c1.decode(result2.getString(2)), Integer.toString(result2.getInt(3)), result2.getString(4), result2.getString(5)});
            }

            if (model.getRowCount() < 4) {
                model.setRowCount(4);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        } finally {
            con.close();
            statement.close();
        }
    }

    void timechecker() throws SQLException {
        Cipher c1 = new Cipher();
        Connection con = ConnectionProvider.getconnection();
        Statement statement = con.createStatement();
        try {

            String query = "SELECT * FROM  librarypark WHERE libparkavailability='" + c1.encode("Not Available") + "' ";
            ResultSet result2 = statement.executeQuery(query);
            if (result2.next()) {
                LocalDateTime now = LocalDateTime.now();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("hh:mm:ss");
                String str = formatter.format(now);
                Time a = java.sql.Time.valueOf(str);
                Time b = result2.getTime(5);
                int c = a.compareTo(b);
                if (c > 0) {
                    String query2 = "UPDATE librarypark SET libparkavailability='" + c1.encode("Available") + "',issueid=0,issuetime='00:00:00',duetime='00:00:00' WHERE libparkavailability='" + c1.encode("Not Available") + "'";
                    statement.executeUpdate(query2);
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        } finally {
            con.close();
            statement.close();
        }
    }

