    private void jTextField1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField1MouseClicked
        boolean val1 = false;
        boolean val2 = false;
        boolean val3 = false;
        boolean val4 = false;

        if (!pwtf.getText().equals("")) {
            val1 = true;
        } else {
            JOptionPane.showMessageDialog(this, "Please Enter Password To Continue.");
        }
        if (!accnotf.getText().equals("")) {
            val2 = true;
        } else {
            JOptionPane.showMessageDialog(this, "Please Enter Credit Card Number To Continue.");
        }
        if (accnotf.getText().matches("^[0-9]*$")) {
            val3 = true;
        } else {
            JOptionPane.showMessageDialog(this, "'Credit Card Number' Field can only contain 0-9 ");
        }
        if (accnotf.getText().length() == 12) {
            val4 = true;
        } else {
            JOptionPane.showMessageDialog(this, "Wrong Format For Credit Card Number !");
        }
        if (val1 && val2 & val3 && val4) {
            try {
                submit();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_jTextField1MouseClicked

