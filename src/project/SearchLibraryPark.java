        Cipher cipher = new Cipher();
        if (searchtf1.getText().equals("") && combobox1.getSelectedIndex() != 3) {
            JOptionPane.showMessageDialog(this, "Please Fill Search Bar!!!");
        } else {
            if (combobox1.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Please Select Option To Filter Result!!!");
            } else {
                if (combobox1.getSelectedIndex() == 1) {
                    query1 = "SELECT * FROM librarypark WHERE libpark_id='" + cipher.encode(searchtf1.getText()) + "' ";
                }
                if (combobox1.getSelectedIndex() == 2) {
                    query1 = "SELECT * FROM librarypark WHERE issueid='" + cipher.encode(searchtf1.getText()) + "'";
                }
            }
        }
        if (combobox1.getSelectedIndex() == 3) {
            query1 = "SELECT * FROM librarypark";
        }
        try {
            printtable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        searchtf1.setText("");
        combobox1.setSelectedIndex(0);
